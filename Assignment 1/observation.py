class Observation:
    def __init__(self, animalNo= "", date = "", time = "", weight = 0.0, temperature = 0.0, note = "", staffName = "", staffSurname = ""):

        self.animalNo = animalNo
        self.date = date
        self.time = time
        self.weight = weight
        self.temperature = temperature
        self.note = note
        self.staffName = staffName
        self.staffSurname = staffSurname